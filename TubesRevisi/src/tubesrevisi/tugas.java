 private void tampilkanTugas(){
        DefaultTableModel tb1 = new DefaultTableModel();
        tb1.addColumn("No");
        tb1.addColumn("ID");
        tb1.addColumn("Deskripsi");
        tb1.addColumn("Kategori");
        tb1.addColumn("Tenggat");
        
        try {
            int counter = 1; 
//          1.  Query
            String query = "SELECT * FROM tb_tugas";
            
//          2.  Fungsi koneksi
            java.sql.Connection Vconn = (java.sql.Connection)koneksiDB.configDB();
            
//          3.  Kirim Paramter fungsi Java ke SQL
            java.sql.Statement s = Vconn.createStatement();
            
//          4.  Eksekusi Query
            java.sql.ResultSet r = s.executeQuery(query);
            
//          5. Looping  
            while (r.next()) {
                tb1.addRow(new Object[]{
                    counter++,r.getString(1),r.getString(2),r.getString(3), r.getString(4)
                });
            }
            tampilTugas.setModel(tb1);
        } catch (Exception e) {
            System.out.println("Terjadi kesalahan saat menambahkan data ke tabel: " + e.getMessage());
        }
    }
