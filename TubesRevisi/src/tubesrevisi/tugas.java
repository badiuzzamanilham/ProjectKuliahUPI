    private void tampilTugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tampilTugasMouseClicked
        // TODO add your handling code here:
         int baris = tampilTugas.rowAtPoint(evt.getPoint());
        
        String ID = tampilTugas.getValueAt(baris, 1).toString();
        id.setText(ID);
        
        String desc = tampilTugas.getValueAt(baris, 2).toString();
        descArea.setText(desc);
        
        String kat = tampilTugas.getValueAt(baris, 3).toString();
        cat.setText(kat);
        
        String tgl = tampilTugas.getValueAt(baris, 4).toString();
        DateFormat dt = new SimpleDateFormat("yyy-MM-dd");
        Date date = null;
        try {
            date = dt.parse(tgl);
        } catch (ParseException ex) {
            Logger.getLogger(tugas.class.getName()).log(Level.SEVERE, null, ex);
        }
        tanggal.setDate(date);
        
    }//GEN-LAST:event_tampilTugasMouseClicked

